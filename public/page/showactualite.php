      <p>Le Cervin (Grand'Bèca en arpitan, Cervino en italien, Matterhorn en allemand) est un sommet alpin de 4 478 mètres d'altitude, situé sur la frontière italo-suisse, entre le canton du Valais et la Vallée d'Aoste.

Le Cervin est la montagne la plus connue de Suisse, notamment pour l'aspect pyramidal qu'elle offre depuis le village de Zermatt, dans la partie alémanique du canton du Valais.

L'ascension par l'arête du Hörnli, le 14 juillet 1865, fut considérée comme le dernier des grands exploits de l'alpinisme dans les Alpes. Mais cette ascension se solda, au début de la descente, par la mort de 4 des 7 membres de la cordée victorieuse1.

Sa face nord est l'une des trois grandes faces nord des Alpes avec celle de l'Eiger et celle des Grandes Jorasses.</p>
