      <p>L’Elbrouz, ou Elbrous1 (russe : Эльбрус, Elbrus ou El'brus2 ; karatchaï-balkar : Минги тау, Miñi taw /miŋŋi taw/), situé dans le nord du Caucase, en Russie, est le point culminant de cette chaîne de montagnes ; avec ses 5 642 mètres, plus élevé que le mont Blanc, il est donc le plus haut sommet de l'Europe. Il s'agit d'un volcan ayant connu des éruptions jusqu'au début de notre ère, et il a fait naître des légendes comme celle voulant que Prométhée ait été enchaîné à ses deux pics principaux pour avoir offert le feu aux hommes. Il est recouvert de nombreux glaciers et, même si l'ascension est techniquement facile et dispose de moyens mécaniques sur l'itinéraire principal, il reste difficile d'accès en raison de ses conditions climatiques rigoureuses et changeantes. Ainsi, le point culminant n'est vaincu qu'en 1874 et la montagne, devenue un symbole de conquête, a fait de nombreux morts. Malgré des problèmes environnementaux dus à la fréquentation, la faune et la flore relativement riches sont protégées par un parc national depuis 1986.</p>
